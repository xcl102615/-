package model.food;

import model.foodType.FoodType;
import model.shop.Shop;
import model.type.Type;
import project.model.BaseObject;

import java.util.List;

/***
 * 食品类
 */
public class Food extends BaseObject {
    private Shop shop;
    //    店铺
    private FoodType foodType;
    //  店铺食品分类
    private Type type;
    //    平台类别
    private String image;
    //  食品图片
    private Double price;
    //    如果商品没有其他规格时候的价格
    private List<FoodFormat>foodFormats;
    //规格
    private Integer monthSaleNum;
    //    月销售
    private Integer saleCount;
    //    销售总量
    private Integer list;
    //  排序方式
    private Integer number;
    //库存数量


    public Shop getShop() {
        return shop;
    }

    public void setShop(Shop shop) {
        this.shop = shop;
    }

    public FoodType getFoodType() {
        return foodType;
    }

    public void setFoodType(FoodType foodType) {
        this.foodType = foodType;
    }

    public Type getType() {
        return type;
    }

    public void setType(Type type) {
        this.type = type;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public List<FoodFormat> getFoodFormats() {
        return foodFormats;
    }

    public void setFoodFormats(List<FoodFormat> foodFormats) {
        this.foodFormats = foodFormats;
    }

    public Integer getMonthSaleNum() {
        return monthSaleNum;
    }

    public void setMonthSaleNum(Integer monthSaleNum) {
        this.monthSaleNum = monthSaleNum;
    }

    public Integer getSaleCount() {
        return saleCount;
    }

    public void setSaleCount(Integer saleCount) {
        this.saleCount = saleCount;
    }

    public Integer getList() {
        return list;
    }

    public void setList(Integer list) {
        this.list = list;
    }

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer number) {
        this.number = number;
    }
}
