import java.util.List;
import java.util.Map;

public class MessageUseropen {

	private int total; // 条数
	private int count; // 总数
	private Map<String, List<String>> data; // 所有opnid
	private String next_openid; // 获取关注用户列表偏移量，不填默认从头开始拉取

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public Map<String, List<String>> getData() {
		return data;
	}

	public void setData(Map<String, List<String>> data) {
		this.data = data;
	}

	public String getNext_openid() {
		return next_openid;
	}

	public void setNext_openid(String next_openid) {
		this.next_openid = next_openid;
	}

	@Override
	public String toString() {
		return "Useropen [total=" + total + ", count=" + count + ", data=" + data + ", next_openid=" + next_openid
				+ "]";
	}
	
}
